// Returns the correctly declined "question" noun for a given count.
// RU/UA have 1 / 2-4 / 5+ forms; ES has singular/plural; ZH has no plural;
// fallback is English-style s-plural. Mirrors the mobile app's questionsLabel.
export function pluralizeQuestions(n, lang) {
  const num = Number(n);
  if (!Number.isFinite(num)) return lang === 'ru' ? 'вопросов' : lang === 'ua' ? 'питань' : lang === 'es' ? 'preguntas' : lang === 'zh' ? '道题' : 'questions';
  if (lang === 'ru' || lang === 'ua') {
    const m10 = num % 10, m100 = num % 100;
    if (m10 === 1 && m100 !== 11) return lang === 'ru' ? 'вопрос' : 'питання';
    if (m10 >= 2 && m10 <= 4 && (m100 < 12 || m100 > 14)) return lang === 'ru' ? 'вопроса' : 'питання';
    return lang === 'ru' ? 'вопросов' : 'питань';
  }
  if (lang === 'es') return num === 1 ? 'pregunta' : 'preguntas';
  if (lang === 'zh') return '道题';
  return num === 1 ? 'question' : 'questions';
}

