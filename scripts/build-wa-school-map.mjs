// Build lib/wa-school-map-data.js: Washington driving schools projected onto
// a brand SVG map. Sources:
//   - DOL WA public locations feed (dol.wa.gov/custom_php/locations-data.php)
//     — official licensed schools with lat/lng, services and LANGUAGES.
//   - Washington boundary from the public-domain US Census states GeoJSON.
// Both school dots and the state outline go through the SAME equirectangular
// projection, so points always land where they belong.
//   node scripts/build-wa-school-map.mjs
import { writeFileSync } from 'node:fs';

const UA = { 'User-Agent': 'DMVSOS-map-build/1.0 (dmvsos.com)' };

// Our language codes ← DOL language names (extras kept as plain labels).
const LANG_MAP = { English: 'en', Russian: 'ru', Spanish: 'es', Chinese: 'zh', Ukrainian: 'ua' };
// Our categories ← DOL categories. DLO offices and CDL skills examiners are
// out of scope for the schools map.
const CAT_MAP = { Driver: 'car', Motorcycle: 'moto', Commercial: 'cdl' };

const locRes = await fetch('https://dol.wa.gov/custom_php/locations-data.php', { headers: UA });
const locJson = await locRes.json();
const items = Array.isArray(locJson) ? locJson : (locJson.locations || locJson.data || Object.values(locJson)[0]);

// Detailed census-derived boundary (~1.4k points) — the coarse us-states file
// flattens Puget Sound into a blob, and the sound IS the beauty of this map.
const geoRes = await fetch('https://raw.githubusercontent.com/glynnbird/usstatesgeojson/master/washington.geojson', { headers: UA });
const geo = await geoRes.json();
const wa = geo.type === 'Feature' ? geo : geo.features.find(f => f.properties.name === 'Washington');
if (!wa) throw new Error('WA feature not found');

// Projection: equirectangular over the WA bounding box (outline + dots).
const polys = wa.geometry.type === 'Polygon' ? [wa.geometry.coordinates] : wa.geometry.coordinates;
let minLon = 180, maxLon = -180, minLat = 90, maxLat = -90;
for (const poly of polys) for (const ring of poly) for (const [lon, lat] of ring) {
  if (lon < minLon) minLon = lon; if (lon > maxLon) maxLon = lon;
  if (lat < minLat) minLat = lat; if (lat > maxLat) maxLat = lat;
}
const W = 1000;
const kx = Math.cos(((minLat + maxLat) / 2) * Math.PI / 180);
const H = Math.round(W * ((maxLat - minLat) / ((maxLon - minLon) * kx)));
const px = (lon) => +(((lon - minLon) / (maxLon - minLon)) * W).toFixed(1);
const py = (lat) => +((1 - (lat - minLat) / (maxLat - minLat)) * H).toFixed(1);

const outline = polys.map(poly => poly.map(ring =>
  'M' + ring.map(([lon, lat]) => `${px(lon)},${py(lat)}`).join('L') + 'Z'
).join('')).join('');

const schools = [];
for (const x of items) {
  const cat = CAT_MAP[x.Category];
  if (!cat) continue;
  const langs = [], extraLangs = [];
  for (const l of x.Languages || []) {
    if (LANG_MAP[l]) langs.push(LANG_MAP[l]); else extraLangs.push(l);
  }
  const addr = String(x.Address || '').replace(/\r?\n/g, ', ');
  const zip = (addr.match(/\b(\d{5})(?:-\d{4})?\b/) || [])[1] || '';
  schools.push({
    id: x.ID,
    name: x.Name,
    city: x.City,
    address: addr,
    zip,
    cat,
    langs,
    extraLangs,
    x: px(x.Longitude),
    y: py(x.Latitude),
    // Partnership fields — the ranking machinery for the schools program:
    // partner schools stay on the map; more QR codes in the school = higher
    // tier = higher placement in search results.
    partner: false,
    qrTier: 0,
  });
}

// Major-city anchors so the map reads as geography, not an abstract blob.
// Well-spaced picks; rank 1 shows at every zoom, rank 2 when zoomed in.
const CITIES = [
  { name: 'Seattle', lat: 47.6062, lon: -122.3321, rank: 1 },
  { name: 'Spokane', lat: 47.6588, lon: -117.4260, rank: 1 },
  { name: 'Tacoma', lat: 47.2529, lon: -122.4443, rank: 1 },
  { name: 'Vancouver', lat: 45.6387, lon: -122.6615, rank: 1 },
  { name: 'Yakima', lat: 46.6021, lon: -120.5059, rank: 1 },
  { name: 'Bellingham', lat: 48.7519, lon: -122.4787, rank: 1 },
  { name: 'Everett', lat: 47.9790, lon: -122.2021, rank: 2 },
  { name: 'Bellevue', lat: 47.6101, lon: -122.2015, rank: 2 },
  { name: 'Olympia', lat: 47.0379, lon: -122.9007, rank: 2 },
  { name: 'Kennewick', lat: 46.2112, lon: -119.1372, rank: 1 },
  { name: 'Wenatchee', lat: 47.4235, lon: -120.3103, rank: 2 },
  { name: 'Port Angeles', lat: 48.1181, lon: -123.4307, rank: 2 },
];
const cities = CITIES.map(c => ({ name: c.name, rank: c.rank, x: px(c.lon), y: py(c.lat) }));

let out = '// GENERATED by scripts/build-wa-school-map.mjs — do not edit by hand.\n';
out += '// Washington licensed driver training schools (DOL WA public locations\n';
out += `// feed, ${new Date().toISOString().slice(0, 10)}), projected for the brand SVG map.\n\n`;
out += `export const WA_MAP = { width: ${W}, height: ${H}, outline: ${JSON.stringify(outline)} };\n\n`;
out += `export const WA_CITIES = ${JSON.stringify(cities)};\n\n`;
out += `export const WA_SCHOOLS = ${JSON.stringify(schools)};\n`;
writeFileSync(new URL('../lib/wa-school-map-data.js', import.meta.url), out);
console.log(`WA map: ${W}x${H}, outline ${outline.length} chars, schools ${schools.length} (car ${schools.filter(s => s.cat === 'car').length} / moto ${schools.filter(s => s.cat === 'moto').length} / cdl ${schools.filter(s => s.cat === 'cdl').length})`);
